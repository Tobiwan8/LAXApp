GO
CREATE DATABASE LAXMovieDB;
GO
USE LAXMovieDB;
GO

CREATE TABLE Movies(
Id INT PRIMARY KEY IDENTITY(1,1),
Title NVARCHAR(50) UNIQUE NOT NULL,
GenreId INT NULL);

CREATE TABLE Ratings (
Id INT PRIMARY KEY IDENTITY(1,1),
MovieId INT NOT NULL,
Rating INT NOT NULL,
CONSTRAINT FK_Ratings_Movies FOREIGN KEY (MovieId) REFERENCES Movies(Id)
);

